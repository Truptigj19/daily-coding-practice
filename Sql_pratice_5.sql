--Q8: Customers with More Orders Than Average Customer..Find customers whose total number of orders is greater than average orders per customer.
select customer_id, count(*) as total_orders
from orders
group by customer_id
having count(*) > (
    select avg(order_count)
    from (
        select customer_id, count(*) as order_count
        from orders
        group by customer_id
    ) as temp
);

--Q9: Revenue of Top 2 Customers..Find total revenue generated by top 2 highest spending customers..Step-wise thinking required
select sum(total_spend) as revenue
from (
    select top 2 customer_id, sum(order_amount) as total_spend
    from orders
    group by customer_id
    order by total_spend desc
) as temp;

--Q10: Customers Who Contribute to Top 50% Revenue..Find customers who contribute to top 50% of total revenue
with temp as (
    select customer_id,
           sum(order_amount) as total_spend
    from orders
    group by customer_id
),
ranked as (
    select *,
           sum(total_spend) over (order by total_spend desc) as running_total,
           sum(total_spend) over () as overall_total
    from temp
)
select customer_id, total_spend
from ranked
where running_total <= 0.5 * overall_total;