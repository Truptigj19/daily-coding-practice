--Q6: Customers Whose Spend is Above Their Country Average
--Find customers whose total spend is greater than average spend of their country..Multi-level aggregation
select c.customer_id
from customers c
join orders o on c.customer_id = o.customer_id
group by c.customer_id, c.country
having sum(o.order_amount) > (
    select avg(total_spend)
    from (
        select c2.country, c2.customer_id, sum(o2.order_amount) as total_spend
        from customers c2
        join orders o2 on c2.customer_id = o2.customer_id
        group by c2.country, c2.customer_id
    ) as temp
    where temp.country = c.country
);


